// Fetch from Supabase
export async function fetchDonors(): Promise<Donor[]> {
  try {
    const { data, error } = await supabase
      .from('donors')
      .select('name, blood, type, wilaya, commune, phone, last_don')
      .order('created_at', { ascending: false });

    if (error) {
      console.error('Supabase fetch error:', error);
      return MOCK_DONORS;
    }

    if (!data || data.length === 0) {
      return MOCK_DONORS;
    }

    return data.map(d => ({
      name: d.name,
      blood: d.blood,
      type: d.type as 'blood' | 'both',
      wilaya: d.wilaya,
      commune: d.commune,
      phone: d.phone,
      lastDon: new Date(d.last_don),
    }));
  } catch (err) {
    console.error('Fetch exception:', err);
    return MOCK_DONORS;
  }
}

// Add to Supabase
export async function addDonorToSupabase(donor: Donor): Promise<boolean> {
  try {
    const { error } = await supabase
      .from('donors')
      .insert([
        {
          name: donor.name,
          blood: donor.blood,
          type: donor.type,
          wilaya: donor.wilaya,
          commune: donor.commune,
          phone: donor.phone,
          last_don: donor.lastDon.toISOString().split('T')[0], // YYYY-MM-DD format
        }
      ]);

    if (error) {
      console.error('Supabase insert error:', error);
      return false;
    }
    return true;
  } catch (err) {
    console.error('Insert exception:', err);
    return false;
  }
}
